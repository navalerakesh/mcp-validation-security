    {
        return severity switch
        {
            ReportSeverity.Critical => HtmlReportTone.Danger,
            ReportSeverity.High => HtmlReportTone.Danger,
            ReportSeverity.Medium => HtmlReportTone.Warning,
            ReportSeverity.Low => HtmlReportTone.Info,
            _ => HtmlReportTone.Success
        };
    }

    private static HtmlReportTone MapTestTone(TestStatus status)
    {
        return status switch
        {
            TestStatus.Passed => HtmlReportTone.Success,
            TestStatus.Failed => HtmlReportTone.Danger,
            TestStatus.Skipped or TestStatus.AuthRequired or TestStatus.Inconclusive => HtmlReportTone.Warning,
            _ => HtmlReportTone.Neutral
        };
    }

    private static HtmlReportTone MapScoreTone(double score)
    {
        if (score >= 88)
        {
            return HtmlReportTone.Success;
        }

        if (score >= 70)
        {
            return HtmlReportTone.Info;
        }

        if (score >= 50)
        {
            return HtmlReportTone.Warning;
        }

        return HtmlReportTone.Danger;
    }

    private static string FormatAuthorityCoverage(int affectedComponents, int totalComponents)
    {
        if (totalComponents > 0)
        {
            var percentage = ValidationFindingAggregator.CalculateCoverageRatio(affectedComponents, totalComponents) * 100;
            return $"{affectedComponents}/{totalComponents} ({percentage:0}%)";
        }

        return affectedComponents.ToString(CultureInfo.InvariantCulture);
    }

    private static string FormatAuthorityLabel(string authority)
    {
        if (string.IsNullOrWhiteSpace(authority))
        {
            return "Unspecified";
        }

        return char.ToUpperInvariant(authority[0]) + authority[1..].ToLowerInvariant();
    }

    private static HtmlReportTone MapErrorRateTone(double errorRate)
    {
        if (errorRate <= 1)
        {
            return HtmlReportTone.Success;
        }

        if (errorRate <= 5)
        {
            return HtmlReportTone.Warning;
        }

        return HtmlReportTone.Danger;
    }

    private static double InvertLatencyToScore(double latencyMs, double idealThreshold, double softThreshold)
    {
        if (latencyMs <= idealThreshold)
        {
            return 95;
        }

        if (latencyMs <= softThreshold)
        {
            return 72;
        }

        return 38;
    }
