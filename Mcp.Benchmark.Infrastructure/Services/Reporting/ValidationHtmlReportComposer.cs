    private static string RenderClientCompatibility(ValidationResult result)
    {
        var compatibility = result.ClientCompatibility!;
        var sb = new StringBuilder();
        sb.AppendLine("      <section class=\"section\">");
        sb.AppendLine("        <div class=\"section-shell\">");
        sb.AppendLine("          <div class=\"section-kicker\">Host Compatibility</div>");
        sb.AppendLine("          <h2 class=\"section-title\">Client Compatibility</h2>");
        sb.AppendLine($"          <p class=\"section-intro\">{compatibility.Assessments.Count} profiles evaluated. The summary bar highlights leadership-level distribution first, followed by durable per-profile notes.</p>");
        sb.AppendLine(RenderDistributionSummary(
            "Compatibility Distribution",
            new[]
            {
                ("Compatible", compatibility.CompatibleCount, HtmlReportTone.Success),
                ("Warnings", compatibility.WarningCount, HtmlReportTone.Warning),
                ("Incompatible", compatibility.IncompatibleCount, HtmlReportTone.Danger)
            }));
        sb.AppendLine("          <div class=\"ledger-list\">");

        foreach (var assessment in compatibility.Assessments)
        {
            var tone = assessment.Status switch
            {
                ClientProfileCompatibilityStatus.Compatible => HtmlReportTone.Success,
                ClientProfileCompatibilityStatus.CompatibleWithWarnings => HtmlReportTone.Warning,
                ClientProfileCompatibilityStatus.Incompatible => HtmlReportTone.Danger,
                _ => HtmlReportTone.Neutral
            };
            var noteworthyRequirements = assessment.Requirements
                .Where(requirement => requirement.Outcome is ClientProfileRequirementOutcome.Warning or ClientProfileRequirementOutcome.Failed)
                .Take(2)
                .ToList();

            sb.AppendLine($"            <article class=\"ledger-entry ledger-entry--{ValidationHtmlReportTheme.ToCssTone(tone)}\">");
            sb.AppendLine("              <div class=\"ledger-rail\">");
            sb.AppendLine($"                <div class=\"ledger-title\">{Encode(assessment.DisplayName)}</div>");
            sb.AppendLine($"                <div class=\"ledger-subtle\">{Encode(assessment.ProfileId)} · {Encode(assessment.Revision)}</div>");
            sb.AppendLine($"                {RenderToneChip(assessment.StatusLabel, tone)}");
            sb.AppendLine($"                <div class=\"check-summary\"><span>P {assessment.PassedRequirements.ToString(CultureInfo.InvariantCulture)}</span><span>W {assessment.WarningRequirements.ToString(CultureInfo.InvariantCulture)}</span><span>F {assessment.FailedRequirements.ToString(CultureInfo.InvariantCulture)}</span></div>");
            sb.AppendLine("              </div>");
            sb.AppendLine("              <div class=\"ledger-body\">");
            sb.AppendLine($"                <p class=\"ledger-copy\">{Encode(assessment.Summary)}</p>");
            if (noteworthyRequirements.Count > 0)
            {
                sb.AppendLine("                <ul class=\"compact-list compact-list--tight\">");
                foreach (var requirement in noteworthyRequirements)
                {
                    sb.AppendLine($"                  <li><strong>{Encode(requirement.Title)}</strong>: {Encode(requirement.Summary)}</li>");
                }
                sb.AppendLine("                </ul>");
            }
            if (!string.IsNullOrWhiteSpace(assessment.DocumentationUrl))
            {
                sb.AppendLine($"                <div class=\"ledger-links\">Reference: <a href=\"{Encode(assessment.DocumentationUrl)}\">Profile documentation</a></div>");
            }
            sb.AppendLine("              </div>");
            sb.AppendLine("            </article>");
        }

        sb.AppendLine("          </div>");
        sb.AppendLine("        </div>");
        sb.AppendLine("      </section>");
        return sb.ToString();
    }

    private static string RenderCapabilitySnapshot(CapabilitySummary snapshot)
    {
        var sb = new StringBuilder();
        sb.AppendLine("      <section class=\"section\">");
        sb.AppendLine("        <div class=\"section-shell\">");
        sb.AppendLine("          <div class=\"section-kicker\">Capability Probe</div>");
        sb.AppendLine("          <h2 class=\"section-title\">Capability Snapshot</h2>");
        var healthyCount = new[] { snapshot.ToolListingSucceeded, snapshot.ResourceListingSucceeded, snapshot.PromptListingSucceeded }.Count(value => value);
        sb.AppendLine("          <p class=\"section-intro\">Probe outcomes are shown as compact operational rows so long notes do not break the layout.</p>");
        sb.AppendLine(RenderDistributionSummary(
            "Probe Outcomes",
            new[]
            {
                ("Healthy", healthyCount, HtmlReportTone.Success),
                ("Needs review", 3 - healthyCount, HtmlReportTone.Warning)
            }));
        sb.AppendLine("          <div class=\"probe-list\">");
        sb.AppendLine(RenderCapabilityRow("Tools/list", snapshot.DiscoveredToolsCount, snapshot.ToolListResponse?.StatusCode, snapshot.ToolListDurationMs, snapshot.ToolListingSucceeded, snapshot.FirstToolName, snapshot.ToolInvocationSucceeded));
        sb.AppendLine(RenderCapabilityRow("Resources/list", snapshot.DiscoveredResourcesCount, snapshot.ResourceListResponse?.StatusCode, snapshot.ResourceListDurationMs, snapshot.ResourceListingSucceeded, null, null));
        sb.AppendLine(RenderCapabilityRow("Prompts/list", snapshot.DiscoveredPromptsCount, snapshot.PromptListResponse?.StatusCode, snapshot.PromptListDurationMs, snapshot.PromptListingSucceeded, null, null));
        sb.AppendLine("          </div>");
        sb.AppendLine("        </div>");
        sb.AppendLine("      </section>");
        return sb.ToString();

        string RenderCapabilityRow(string title, int discovered, int? statusCode, double durationMs, bool succeeded, string? toolName, bool? invocationSucceeded)
        {
            var tone = succeeded ? HtmlReportTone.Success : HtmlReportTone.Warning;
            var status = succeeded ? "Healthy" : "Attention";
            var noteParts = new List<string>
            {
                succeeded ? "List probe completed." : "List probe needs review."
            };

            if (!string.IsNullOrWhiteSpace(toolName))
            {
                noteParts.Add(invocationSucceeded == true
                    ? $"Primary tool {toolName} responded successfully."
                    : $"Primary tool {toolName} could not be invoked.");
            }

            var httpStatus = statusCode.HasValue ? $"HTTP {statusCode.Value}" : "n/a";
            var duration = durationMs > 0 ? $"{durationMs:F1} ms" : "n/a";
            var noteText = string.Join(" ", noteParts);
            return $"""
                        <article class="probe-row probe-row--{ValidationHtmlReportTheme.ToCssTone(tone)}">
                            <div class="probe-row__heading">
                                <div class="ledger-title">{Encode(title)}</div>
                                {RenderToneChip(status, tone)}
                            </div>
                            <div class="probe-row__metrics">
                                <span class="probe-metric"><strong>HTTP</strong> {Encode(httpStatus)}</span>
                                <span class="probe-metric"><strong>Discovered</strong> {Encode(discovered.ToString(CultureInfo.InvariantCulture))}</span>
                                <span class="probe-metric"><strong>Duration</strong> {Encode(duration)}</span>
                            </div>
                            <div class="probe-row__note">{Encode(noteText)}</div>
                        </article>
                        """;
        }
    }

