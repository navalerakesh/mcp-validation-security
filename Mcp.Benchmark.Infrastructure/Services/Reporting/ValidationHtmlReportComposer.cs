            "resources/list" => 3,
            "resources/read" => 4,
            "tools/list" => 5,
            "tools/call" => 6,
            _ => 50
        };
    }

    private static string ResolveAuthenticationScenarioLabel(AuthenticationScenario scenario)
    {
        if (!string.IsNullOrWhiteSpace(scenario.TestType))
        {
            return scenario.TestType.Trim();
        }

        if (!string.IsNullOrWhiteSpace(scenario.Method))
        {
            var methodSuffix = $" - {scenario.Method.Trim()}";
            if (scenario.ScenarioName.EndsWith(methodSuffix, StringComparison.OrdinalIgnoreCase))
            {
                return scenario.ScenarioName[..^methodSuffix.Length].Trim();
            }
        }

        return scenario.ScenarioName;
    }

