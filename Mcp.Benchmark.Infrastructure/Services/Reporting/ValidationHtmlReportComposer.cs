    private static string RenderDistributionSummary(string title, IReadOnlyList<(string Label, int Count, HtmlReportTone Tone)> slices)
    {
        var relevantSlices = slices.Where(slice => slice.Count > 0).ToList();
        if (relevantSlices.Count == 0)
        {
            return string.Empty;
        }

        var total = relevantSlices.Sum(slice => slice.Count);
        var sb = new StringBuilder();
        sb.AppendLine("          <div class=\"distribution-summary\">");
        sb.AppendLine($"            <div class=\"distribution-summary__title\">{Encode(title)}</div>");
        sb.AppendLine("            <div class=\"distribution-summary__cards\">");
        foreach (var slice in relevantSlices)
        {
            var percentage = total > 0 ? (slice.Count * 100.0 / total).ToString("F0", CultureInfo.InvariantCulture) : "0";
            var toneCss = ValidationHtmlReportTheme.ToCssTone(slice.Tone);
            sb.AppendLine($"              <div class=\"distribution-card distribution-card--{toneCss}\">");
            sb.AppendLine($"                <div class=\"distribution-card__label\">{Encode(slice.Label)}</div>");
            sb.AppendLine($"                <div class=\"distribution-card__value\">{slice.Count.ToString(CultureInfo.InvariantCulture)}</div>");
            sb.AppendLine($"                <div class=\"distribution-card__meta\">{percentage}% of observed outcomes</div>");
            sb.AppendLine("              </div>");
        }
        sb.AppendLine("            </div>");
        sb.AppendLine("            <div class=\"distribution-bar\">");
        foreach (var slice in relevantSlices)
        {
            var toneCss = ValidationHtmlReportTheme.ToCssTone(slice.Tone);
            sb.AppendLine($"              <span class=\"distribution-segment distribution-segment--{toneCss}\" style=\"flex:{slice.Count};\"></span>");
        }
        sb.AppendLine("            </div>");
        sb.AppendLine("            <div class=\"distribution-legend\">");
        foreach (var slice in relevantSlices)
        {
            var percentage = total > 0 ? (slice.Count * 100.0 / total).ToString("F0", CultureInfo.InvariantCulture) : "0";
            var toneCss = ValidationHtmlReportTheme.ToCssTone(slice.Tone);
            sb.AppendLine($"              <span class=\"distribution-legend__item\"><span class=\"distribution-swatch distribution-swatch--{toneCss}\"></span>{Encode(slice.Label)} {slice.Count} ({percentage}%)</span>");
        }
        sb.AppendLine("            </div>");
        sb.AppendLine("          </div>");
        return sb.ToString();
    }

    private static IReadOnlyList<(string Label, int Count, HtmlReportTone Tone)> BuildSeverityDistribution(
        IReadOnlyList<ComplianceViolation> violations,
        IReadOnlyList<SecurityVulnerability> vulnerabilities)
    {
        var critical = violations.Count(violation => violation.Severity == ViolationSeverity.Critical)
            + vulnerabilities.Count(vulnerability => vulnerability.Severity == VulnerabilitySeverity.Critical);
        var high = violations.Count(violation => violation.Severity == ViolationSeverity.High)
            + vulnerabilities.Count(vulnerability => vulnerability.Severity == VulnerabilitySeverity.High);
        var medium = violations.Count(violation => violation.Severity == ViolationSeverity.Medium)
            + vulnerabilities.Count(vulnerability => vulnerability.Severity == VulnerabilitySeverity.Medium);
        var low = violations.Count(violation => violation.Severity == ViolationSeverity.Low)
            + vulnerabilities.Count(vulnerability => vulnerability.Severity == VulnerabilitySeverity.Low);

        return new List<(string Label, int Count, HtmlReportTone Tone)>
        {
            ("Critical", critical, HtmlReportTone.Danger),
            ("High", high, HtmlReportTone.Danger),
            ("Medium", medium, HtmlReportTone.Warning),
            ("Low", low, HtmlReportTone.Info)
        };
    }

    private static (string Label, HtmlReportTone Tone) ResolveAuthenticationScenarioOutcome(AuthenticationScenario scenario)
    {
        if (scenario.AssessmentDisposition != AuthenticationAssessmentDisposition.Unknown)
        {
            return scenario.AssessmentDisposition switch
            {
                AuthenticationAssessmentDisposition.StandardsAligned => ("Aligned", HtmlReportTone.Success),
                AuthenticationAssessmentDisposition.SecureCompatible => ("Review", HtmlReportTone.Warning),
                AuthenticationAssessmentDisposition.Insecure => ("Insecure", HtmlReportTone.Danger),
                AuthenticationAssessmentDisposition.Informational => ("Observed", HtmlReportTone.Info),
                AuthenticationAssessmentDisposition.Inconclusive => ("Observed", HtmlReportTone.Info),
                _ => ("Observed", HtmlReportTone.Info)
            };
        }

        if (scenario.IsStandardsAligned)
        {
            return ("Aligned", HtmlReportTone.Success);
        }

        if (scenario.IsSecure)
        {
            return ("Review", HtmlReportTone.Warning);
        }

        if (!string.IsNullOrWhiteSpace(scenario.Analysis))
        {
            if (scenario.Analysis.Contains("ALIGNED", StringComparison.OrdinalIgnoreCase)
                || scenario.Analysis.Contains("accepted and request processing proceeded", StringComparison.OrdinalIgnoreCase))
            {
                return ("Aligned", HtmlReportTone.Success);
            }

            if (scenario.Analysis.Contains("COMPATIBLE", StringComparison.OrdinalIgnoreCase)
                || scenario.Analysis.Contains("application layer", StringComparison.OrdinalIgnoreCase))
            {
                return ("Review", HtmlReportTone.Warning);
            }
        }

        if (!string.IsNullOrWhiteSpace(scenario.WwwAuthenticateHeader))
        {
            return ("Aligned", HtmlReportTone.Success);
        }

        if (string.Equals(scenario.StatusCode, "400", StringComparison.OrdinalIgnoreCase)
            || string.Equals(scenario.StatusCode, "401", StringComparison.OrdinalIgnoreCase)
            || string.Equals(scenario.StatusCode, "403", StringComparison.OrdinalIgnoreCase))
        {
            return ("Review", HtmlReportTone.Warning);
        }

        return ("Observed", HtmlReportTone.Info);
    }

