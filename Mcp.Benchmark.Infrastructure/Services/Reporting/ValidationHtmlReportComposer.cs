    private static string RenderAuthenticationScenarioBlock(string label, string? value, bool emphasize = false)
    {
        var modifier = emphasize ? " auth-scenario__block--emphasis" : string.Empty;
        var displayValue = string.Equals(label, "Interpretation", StringComparison.OrdinalIgnoreCase)
            ? NormalizeAuthenticationInterpretation(value)
            : string.IsNullOrWhiteSpace(value) ? "—" : value;
        return $"                    <div class=\"auth-scenario__block{modifier}\"><div class=\"auth-scenario__block-label\">{Encode(label)}</div><div class=\"auth-scenario__block-value\">{Encode(displayValue)}</div></div>";
    }

    private static string NormalizeAuthenticationInterpretation(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return "—";
        }

        var normalized = value.Trim();
        foreach (var prefix in new[]
        {
            "✅ ALIGNED:",
            "ALIGNED:",
            "⚠️ COMPATIBLE:",
            "COMPATIBLE:",
            "⚠️ REVIEW:",
            "REVIEW:",
            "⚠️ INSECURE:",
            "INSECURE:",
            "ℹ️ OBSERVED:",
            "OBSERVED:"
        })
        {
            if (normalized.StartsWith(prefix, StringComparison.OrdinalIgnoreCase))
            {
                normalized = normalized[prefix.Length..].Trim();
                break;
            }
        }

        return normalized;
    }

