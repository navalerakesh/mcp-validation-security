    private static string RenderSecurityDetails(ValidationResult result)
    {
        if (result.SecurityTesting == null)
        {
            return string.Empty;
        }

        var security = result.SecurityTesting;
        var toneCss = ValidationHtmlReportTheme.ToCssTone(MapScoreTone(security.SecurityScore));
        var authScenarioCount = security.AuthenticationTestResult?.TestScenarios?.Count ?? 0;
        var abstractItems = new (string Label, string Value, HtmlReportTone Tone)[]
        {
            ("Security Score", $"{security.SecurityScore:F1}%", MapScoreTone(security.SecurityScore)),
            ("Vulnerabilities", security.Vulnerabilities.Count.ToString(CultureInfo.InvariantCulture), security.Vulnerabilities.Count > 0 ? HtmlReportTone.Danger : HtmlReportTone.Success),
            ("Auth Scenarios", authScenarioCount.ToString(CultureInfo.InvariantCulture), authScenarioCount > 0 ? HtmlReportTone.Info : HtmlReportTone.Neutral),
            ("Attack Sims", (security.AttackSimulations?.Count ?? 0).ToString(CultureInfo.InvariantCulture), HtmlReportTone.Info)
        };
        var sb = new StringBuilder();
        sb.AppendLine(RenderSectionCardOpen(
            "Security Evidence",
            "Security Assessment Details",
            $"Security score {security.SecurityScore:F1}% with {security.Vulnerabilities.Count} recorded vulnerability finding(s).",
            abstractItems,
            MapScoreTone(security.SecurityScore),
            openByDefault: false));

        if (security.AuthenticationTestResult?.TestScenarios?.Any() == true)
        {
            var authenticationScenarios = security.AuthenticationTestResult.TestScenarios
                .Select(scenario => new
                {
                    Scenario = scenario,
                    Outcome = ResolveAuthenticationScenarioOutcome(scenario)
                })
                .ToList();
            var groupedAuthenticationScenarios = authenticationScenarios
                .GroupBy(entry => ResolveAuthenticationSurface(entry.Scenario))
                .OrderBy(group => GetAuthenticationSurfaceOrder(group.Key))
                .ThenBy(group => group.Key, StringComparer.OrdinalIgnoreCase)
                .ToList();

            sb.AppendLine("          <div class=\"ledger-group\">");
            sb.AppendLine("            <h3 class=\"minor-title\">Authentication Scenarios</h3>");
            sb.AppendLine(RenderDistributionSummary(
                "Authentication Outcome Distribution",
                authenticationScenarios
                    .GroupBy(entry => entry.Outcome.Label)
                    .Select(group => (group.Key, group.Count(), group.First().Outcome.Tone))
                    .ToList()));
            sb.AppendLine("            <div class=\"auth-surface-list\">");
            foreach (var group in groupedAuthenticationScenarios)
            {
                var entries = group.ToList();
                var alignedCount = entries.Count(entry => entry.Outcome.Tone == HtmlReportTone.Success);
                var reviewCount = entries.Count(entry => entry.Outcome.Tone is HtmlReportTone.Warning or HtmlReportTone.Danger);
                var observedCount = entries.Count(entry => entry.Outcome.Tone is HtmlReportTone.Info or HtmlReportTone.Neutral);
                var summaryParts = new List<string> { $"{entries.Count} scenarios" };
                if (alignedCount > 0)
                {
                    summaryParts.Add($"{alignedCount} aligned");
                }

                if (reviewCount > 0)
                {
                    summaryParts.Add($"{reviewCount} review");
                }

                if (observedCount > 0)
                {
                    summaryParts.Add($"{observedCount} observed");
                }

                sb.AppendLine("              <section class=\"auth-surface\">");
                sb.AppendLine("                <div class=\"auth-surface__header\">");
                sb.AppendLine("                  <div>");
                sb.AppendLine("                    <div class=\"ledger-kicker\">Test surface</div>");
                sb.AppendLine($"                    <div class=\"auth-surface__title\">{Encode(group.Key)}</div>");
                sb.AppendLine($"                    <div class=\"auth-surface__meta\">{Encode(string.Join(" · ", summaryParts))}</div>");
                sb.AppendLine("                  </div>");
                sb.AppendLine("                  <div class=\"ledger-chip-row\">");
                if (alignedCount > 0)
                {
                    sb.AppendLine($"                    {RenderToneChip($"{alignedCount} aligned", HtmlReportTone.Success)}");
                }

                if (reviewCount > 0)
                {
                    sb.AppendLine($"                    {RenderToneChip($"{reviewCount} review", HtmlReportTone.Warning)}");
                }

                if (observedCount > 0)
                {
                    sb.AppendLine($"                    {RenderToneChip($"{observedCount} observed", HtmlReportTone.Info)}");
                }

                sb.AppendLine("                  </div>");
                sb.AppendLine("                </div>");
                sb.AppendLine("                <div class=\"auth-scenario-list\">");
                foreach (var entry in entries)
                {
                    var scenario = entry.Scenario;
                    var tone = entry.Outcome.Tone;
                    var scenarioToneCss = ValidationHtmlReportTheme.ToCssTone(tone);
                    var scenarioLabel = ResolveAuthenticationScenarioLabel(scenario);
                    var useCompactLayout = tone == HtmlReportTone.Success;
                    var scenarioLayoutCss = useCompactLayout ? " auth-scenario--compact" : string.Empty;
                    sb.AppendLine($"                  <article class=\"auth-scenario auth-scenario--{scenarioToneCss}{scenarioLayoutCss}\">");
                    sb.AppendLine("                    <div class=\"auth-scenario__header\">");
                    sb.AppendLine($"                      <div class=\"auth-scenario__title\">{Encode(scenarioLabel)}</div>");
                    sb.AppendLine($"                      {RenderToneChip(entry.Outcome.Label, tone)}");
                    sb.AppendLine("                    </div>");

                    if (useCompactLayout)
                    {
                        sb.AppendLine("                    <div class=\"auth-scenario__metrics auth-scenario__metrics--dense\">");
                        sb.AppendLine(RenderAuthenticationScenarioMetric("Expected", scenario.ExpectedBehavior));
                        sb.AppendLine(RenderAuthenticationScenarioMetric("Observed", scenario.ActualBehavior));
                        if (!string.IsNullOrWhiteSpace(scenario.StatusCode))
                        {
                            sb.AppendLine(RenderAuthenticationScenarioMetric("HTTP", scenario.StatusCode));
                        }
                        sb.AppendLine("                    </div>");

                        sb.AppendLine("                    <div class=\"auth-scenario__detail-grid\">");
                        if (!string.IsNullOrWhiteSpace(scenario.WwwAuthenticateHeader))
                        {
                            sb.AppendLine(RenderAuthenticationScenarioBlock("Challenge", scenario.WwwAuthenticateHeader));
                        }
                        sb.AppendLine(RenderAuthenticationScenarioBlock("Interpretation", scenario.Analysis, emphasize: true));
                        sb.AppendLine("                    </div>");
                    }
                    else
                    {
                        sb.AppendLine("                    <div class=\"auth-scenario__comparison\">");
                        sb.AppendLine(RenderAuthenticationScenarioFact("Expected", scenario.ExpectedBehavior));
                        sb.AppendLine(RenderAuthenticationScenarioFact("Observed", scenario.ActualBehavior));
                        sb.AppendLine("                    </div>");

                        if (!string.IsNullOrWhiteSpace(scenario.StatusCode))
                        {
                            sb.AppendLine("                    <div class=\"auth-scenario__metrics\">");
                            sb.AppendLine(RenderAuthenticationScenarioMetric("HTTP", scenario.StatusCode));
                            sb.AppendLine("                    </div>");
                        }

                        if (!string.IsNullOrWhiteSpace(scenario.WwwAuthenticateHeader))
                        {
                            sb.AppendLine(RenderAuthenticationScenarioBlock("Challenge", scenario.WwwAuthenticateHeader));
                        }

                        sb.AppendLine(RenderAuthenticationScenarioBlock("Interpretation", scenario.Analysis, emphasize: true));
                    }
                    sb.AppendLine("                  </article>");
                }
                sb.AppendLine("                </div>");
                sb.AppendLine("              </section>");
            }
            sb.AppendLine("            </div>");
            sb.AppendLine("          </div>");
        }

        if (security.AttackSimulations?.Any() == true)
        {
            var blockedCount = security.AttackSimulations.Count(attack => AttackSimulationOutcomeResolver.Resolve(attack) == AttackSimulationOutcome.Blocked);
            var reflectedCount = security.AttackSimulations.Count(attack => AttackSimulationOutcomeResolver.Resolve(attack) == AttackSimulationOutcome.Detected);
            var skippedCount = security.AttackSimulations.Count(attack => AttackSimulationOutcomeResolver.Resolve(attack) == AttackSimulationOutcome.Skipped);
            sb.AppendLine("          <div class=\"ledger-group\">");
            sb.AppendLine("            <h3 class=\"minor-title\">Attack Simulation Outcomes</h3>");
            sb.AppendLine(RenderDistributionSummary(
                "Attack Outcome Distribution",
                BuildAttackOutcomeSummary(blockedCount, reflectedCount, skippedCount)));
            sb.AppendLine("            <div class=\"ledger-list\">");
            foreach (var attack in security.AttackSimulations)
            {
                var (resultLabel, resultTone) = AttackSimulationOutcomeResolver.Resolve(attack) switch
                {
                    AttackSimulationOutcome.Skipped => ("Skipped", HtmlReportTone.Info),
                    AttackSimulationOutcome.Blocked => ("Blocked", HtmlReportTone.Success),
                    _ => ("Reflected", HtmlReportTone.Warning)
                };
                var response = string.IsNullOrWhiteSpace(attack.ServerResponse) ? "—" : attack.ServerResponse.Replace("\n", " ");
                var probeContext = attack.ProbeContexts?.FirstOrDefault();
                sb.AppendLine($"              <article class=\"ledger-entry ledger-entry--{ValidationHtmlReportTheme.ToCssTone(resultTone)}\">");
                sb.AppendLine("                <div class=\"ledger-rail\">");
                sb.AppendLine($"                  <div class=\"ledger-title\">{Encode(attack.AttackVector)}</div>");
                sb.AppendLine($"                  {RenderToneChip(resultLabel, resultTone)}");
                sb.AppendLine("                </div>");
                sb.AppendLine("                <div class=\"ledger-body\">");
                sb.AppendLine($"                  <p class=\"ledger-copy\">{Encode(attack.Description)}</p>");
                if (probeContext != null)
                {
                    var suffix = attack.ProbeContexts!.Count > 1 ? $" (+{attack.ProbeContexts.Count - 1})" : string.Empty;
                    sb.AppendLine($"                  <div class=\"ledger-code-block\"><strong>Probe</strong><code>{Encode(FormatProbeContext(probeContext) + suffix)}</code></div>");
                }
                sb.AppendLine($"                  <div class=\"ledger-code-block\"><strong>Server response</strong><code>{Encode(response)}</code></div>");
                sb.AppendLine("                </div>");
                sb.AppendLine("              </article>");
            }
            sb.AppendLine("            </div>");
            sb.AppendLine("          </div>");
        }

        sb.AppendLine(RenderSectionCardClose());
        return sb.ToString();
    }

    private static (string Label, int Count, HtmlReportTone Tone)[] BuildAttackOutcomeSummary(int blockedCount, int reflectedCount, int skippedCount)
    {
        var items = new List<(string Label, int Count, HtmlReportTone Tone)>
        {
            ("Blocked", blockedCount, HtmlReportTone.Success),
            ("Reflected", reflectedCount, HtmlReportTone.Warning)
        };

        if (skippedCount > 0)
        {
            items.Add(("Skipped", skippedCount, HtmlReportTone.Info));
        }

        return items.ToArray();
    }

