    private static double InvertLatencyToScore(double latencyMs, double idealThreshold, double softThreshold)
    {
        if (latencyMs <= idealThreshold)
        {
            return 95;
        }

        if (latencyMs <= softThreshold)
        {
            return 72;
        }

        return 38;
    }
