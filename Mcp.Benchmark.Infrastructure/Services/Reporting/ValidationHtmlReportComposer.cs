    private static string RenderRuleInsights(ValidationResult result, ReportingConfig reportConfig)
    {
        var violations = result.ProtocolCompliance?.Violations ?? new List<ComplianceViolation>();
        var vulnerabilities = result.SecurityTesting?.Vulnerabilities ?? new List<SecurityVulnerability>();
        if (!violations.Any() && !vulnerabilities.Any())
        {
            return string.Empty;
        }

        var sb = new StringBuilder();
        sb.AppendLine("      <section class=\"section\">");
        sb.AppendLine("        <div class=\"section-shell\">");
        sb.AppendLine("          <div class=\"section-kicker\">Structured Evidence</div>");
        sb.AppendLine("          <h2 class=\"section-title\">Rule & Severity Insights</h2>");
        sb.AppendLine("          <p class=\"section-intro\">Leadership summary comes first. Detailed findings are then rendered as evidence entries instead of narrow tables.</p>");
        sb.AppendLine(RenderDistributionSummary(
            "Finding Distribution",
            BuildSeverityDistribution(violations, vulnerabilities)));

        if (violations.Any())
        {
            sb.AppendLine(RenderViolationLedger(violations, reportConfig.IncludeSpecReferences));
        }

        if (vulnerabilities.Any())
        {
            sb.AppendLine(RenderVulnerabilityLedger(vulnerabilities));
        }

        sb.AppendLine("        </div>");
        sb.AppendLine("      </section>");
        return sb.ToString();
    }

    private static string RenderViolationLedger(IReadOnlyList<ComplianceViolation> violations, bool includeSpecReferences)
    {
        var sb = new StringBuilder();
        sb.AppendLine("          <div class=\"ledger-group\">");
        sb.AppendLine("            <h3 class=\"minor-title\">Protocol and Compliance Findings</h3>");
        sb.AppendLine("            <div class=\"ledger-list\">");
        foreach (var violation in violations.Take(40))
        {
            var tone = MapViolationTone(violation.Severity);
            sb.AppendLine($"              <article class=\"ledger-entry ledger-entry--{ValidationHtmlReportTheme.ToCssTone(tone)}\">");
            sb.AppendLine("                <div class=\"ledger-rail\">");
            sb.AppendLine($"                  <div class=\"ledger-kicker\">Rule</div>");
            sb.AppendLine($"                  {BuildRuleCell(violation.CheckId, violation.Rule)}");
            sb.AppendLine($"                  <div class=\"ledger-chip-row\">{RenderToneChip(ValidationRuleSourceClassifier.GetLabel(violation), HtmlReportTone.Info)} {RenderToneChip(violation.Severity.ToString(), tone)}</div>");
            sb.AppendLine($"                  <div class=\"ledger-subtle\">{Encode(violation.Category ?? "Uncategorized")}</div>");
            sb.AppendLine("                </div>");
            sb.AppendLine("                <div class=\"ledger-body\">");
            sb.AppendLine($"                  <div class=\"ledger-title\">{Encode(violation.Description)}</div>");
            if (!string.IsNullOrWhiteSpace(violation.Recommendation))
            {
                sb.AppendLine($"                  <p class=\"ledger-copy\"><strong>Remediation:</strong> {Encode(violation.Recommendation)}</p>");
            }
